							<p> 4. D’informer et d’harmoniser les méthodologies des travaux statistiques en RDC;</p>

							<p> 5. De publier et de diffuser, sous réserve de l’intérêt national, les résultats de ses travaux;</p>

							<p> 6. D’effectuer et de faire effectuer, à la demande du Gouvernement ou de sa propre initiative, des études d’ordre démographique, économique et social ;</p>

							<p> 7. De promouvoir le développement de la science statistique au moyen des recherches théoriques et de pourvoir à la formation du personnel chargé des travaux statistiques ;</p>

