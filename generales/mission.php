							<p> 9. De contribuer à l’amélioration de l’organisation des services statistiques en RDC ;</p>

							<p> 10. D’assurer la liaison avec les Services des Statistiques interétatiques, avec ceux des pays étrangers et des Organisations Internationales. </p>



