						<p>  <img src="http://localhost/ins-nordkivu/assets/img/publication/mission.jpg" alt="" class="img-rounded pull-right" width="300" >1. D’établir, de collecter et de tenir à jour des statistiques relatives à l’état et au mouvement des personnes et des biens ;</p>

