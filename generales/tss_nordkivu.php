  De nos jours, la statistique est devenue incontournable dans la gestion efficiente d’une organisation ou de tout un pays. La demande en données statistiques de qualité est de plus en plus croissante, de même que la production de la bonne information statistique. Pour répondre à ces besoins, le Gouvernement de la République Démocratique du Congo (RDC), à travers l’Institut National de la Statistique (INS) a mis en place un programme de formation des statisticiens de tous les niveaux, conformément à son programme de rajeunissement et de renforcement des effectifs, avec l’appui de la Banque Mondiale à travers le Projet de Développement des Statistiques, PDS en sigle.
</p>
<p style="text-align: justify; padding-right :4px;">
C’est dans ce cadre que l’Institut National de la Statistique a diligenté une évaluation institutionnelle des systèmes d’enseignement dans le domaine de la statistique, étant la pépinière professionnelle de l’INS par excellence, avec comme objectif de disposer d’une main d’œuvre répondant directement au profil des Techniciens recherchés.
Cette évaluation a ciblé trois Instituts Supérieurs de Statistique (ISS) en RDC pour renforcer la qualité de la formation et la hisser au niveau des Ecoles régionales de Statistique en Afrique francophone.
</p>
<p>
Il s’agit de :
</p>
-	l’Institut Supérieur de Statistique de Kinshasa;</br>
-	l’Institut Supérieur de Statistique de Lubumbashi; et </br>
-	l’Institut Supérieur de Statistique et de Nouvelles Technologies de Goma. </br>
<p>
Pour ce faire, le Projet a fait appel à l’appui technique de l’Ecole Nationale Supérieure de Statistique et d’Economie Appliquée (ENSEA d’Abidjan), qui est une des Ecoles d’excellence en Afrique.
Les activités convenues entre l’INS et les Ecoles de statistique dans le cadre de nos contrats de collaboration tournent autour de 4 axes d’intervention, à savoir:
</p></br>
1.	La refonte des programmes de formation ;</br>
2.	Le renforcement des capacités des ISS en ressources humaines ;</br>
3.	Le renforcement des infrastructures et </br>
4.	La Formation spéciale de 175 Techniciens Supérieurs de la Statistique (TSS), pour répondre aux besoins urgents en Statisticiens à l’INS, à raison de 80 à Kinshasa, 50 à Lubumbashi et 45 à Goma.</br>
</br>
C’est dans ce cadre que l’exécution du contrat débute notamment par le lancement du programme de formation des Techniciens Supérieurs de la Statistique dans cette Province.  En termes de qualité, la formation sera assurée dans les mêmes standards et le même contenu des cours que ceux dispensés dans de Grandes Ecoles de Statistiques Africaines.

